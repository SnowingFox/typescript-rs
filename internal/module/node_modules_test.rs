// Go: internal/module/resolver.go:loadModuleFromNearestNodeModulesDirectoryWorker (ancestor search)
#[test]
fn node_modules_ancestor_search() {
    let files = [
        ("/repo/node_modules/pkg/index.d.ts", ""),
        (
            "/repo/node_modules/pkg/package.json",
            r#"{"name":"pkg","types":"index.d.ts"}"#,
        ),
        ("/repo/src/deep/nested/a.ts", ""),
    ];
    let r = bundler(&files);
    let (res, _) = r.resolve_module_name(
        "pkg",
        "/repo/src/deep/nested/a.ts",
        ModuleKind::EsNext,
        None,
    );
    assert!(res.is_resolved());
}
