//! `node_modules` directory traversal: nearest-ancestor search, the per-folder
//! `@types` lookup, and the specific-package loader (with the trailing-slash
//! candidate normalization from TS issue #3526).
//!
//! Split from Go `internal/module/resolver.go` (PORTING.md §2).

use tsgo_core::compileroptions::ModuleKind;
use tsgo_diagnostics::{
    DIRECTORY_0_DOES_NOT_EXIST_SKIPPING_ALL_LOOKUPS_IN_IT,
    SEARCHING_ALL_ANCESTOR_NODE_MODULES_DIRECTORIES_FOR_FALLBACK_EXTENSIONS_COLON_0,
    SEARCHING_ALL_ANCESTOR_NODE_MODULES_DIRECTORIES_FOR_PREFERRED_EXTENSIONS_COLON_0,
};
use tsgo_packagejson::JsonValueType;
use tsgo_tspath as tspath;

use crate::state::{PackageJsonInfo, ResolutionState};
use crate::util::parse_package_name;
use crate::{
    continue_searching, try_parse_patterns, write_trace, Extensions, Loader,
    NodeResolutionFeatures, Resolved, ResolvedExt, ResolvedInner,
};

impl ResolutionState<'_> {
    // Go: internal/module/resolver.go:resolutionState.loadModuleFromNearestNodeModulesDirectory
    pub(crate) fn load_module_from_nearest_node_modules_directory(
        &mut self,
        types_scope_only: bool,
    ) -> Resolved {
        let mut mode = ModuleKind::CommonJs;
        if self.esm_mode || self.condition_matches("import") {
            mode = ModuleKind::EsNext;
        }
        let priority_extensions =
            self.extensions & (Extensions::TYPE_SCRIPT | Extensions::DECLARATION);
        let secondary_extensions =
            self.extensions & !(Extensions::TYPE_SCRIPT | Extensions::DECLARATION);
        if !priority_extensions.is_empty() {
            if self.tracer.is_some() {
                let s = priority_extensions.to_string();
                write_trace(
                    &mut self.tracer,
                    &SEARCHING_ALL_ANCESTOR_NODE_MODULES_DIRECTORIES_FOR_PREFERRED_EXTENSIONS_COLON_0,
                    &[&s],
                );
            }
            let result = self.load_module_from_nearest_node_modules_directory_worker(
                priority_extensions,
                mode,
                types_scope_only,
            );
            if !result.should_continue_searching() {
                return result;
            }
        }
        if !secondary_extensions.is_empty() && !types_scope_only {
            if self.tracer.is_some() {
                let s = secondary_extensions.to_string();
                write_trace(
                    &mut self.tracer,
                    &SEARCHING_ALL_ANCESTOR_NODE_MODULES_DIRECTORIES_FOR_FALLBACK_EXTENSIONS_COLON_0,
                    &[&s],
                );
            }
            return self.load_module_from_nearest_node_modules_directory_worker(
                secondary_extensions,
                mode,
                types_scope_only,
            );
        }
        continue_searching()
    }

    // Go: internal/module/resolver.go:resolutionState.loadModuleFromNearestNodeModulesDirectoryWorker
    pub(crate) fn load_module_from_nearest_node_modules_directory_worker(
        &mut self,
        ext: Extensions,
        _mode: ModuleKind,
        types_scope_only: bool,
    ) -> Resolved {
        let containing_directory = self.containing_directory.clone();
        let (result, _) =
            tspath::for_each_ancestor_directory::<Resolved>(&containing_directory, |directory| {
                if tspath::get_base_file_name(directory) != "node_modules" {
                    let result = self.load_module_from_immediate_node_modules_directory(
                        ext,
                        directory,
                        types_scope_only,
                    );
                    let stop = !result.should_continue_searching();
                    (result, stop)
                } else {
                    (continue_searching(), false)
                }
            });
        result
    }

    // Go: internal/module/resolver.go:resolutionState.loadModuleFromImmediateNodeModulesDirectory
    pub(crate) fn load_module_from_immediate_node_modules_directory(
        &mut self,
        extensions: Extensions,
        directory: &str,
        types_scope_only: bool,
    ) -> Resolved {
        let node_modules_folder = tspath::combine_paths(directory, &["node_modules"]);
        if !self
            .resolver
            .host
            .fs()
            .directory_exists(&node_modules_folder)
        {
            write_trace(
                &mut self.tracer,
                &DIRECTORY_0_DOES_NOT_EXIST_SKIPPING_ALL_LOOKUPS_IN_IT,
                &[&node_modules_folder],
            );
            return continue_searching();
        }

        if !types_scope_only {
            let name = self.name.clone();
            let package_result = self.load_module_from_specific_node_modules_directory(
                extensions,
                &name,
                &node_modules_folder,
            );
            if !package_result.should_continue_searching() {
                return package_result;
            }
        }

        if extensions.contains(Extensions::DECLARATION) {
            let node_modules_at_types = tspath::combine_paths(&node_modules_folder, &["@types"]);
            if !self
                .resolver
                .host
                .fs()
                .directory_exists(&node_modules_at_types)
            {
                write_trace(
                    &mut self.tracer,
                    &DIRECTORY_0_DOES_NOT_EXIST_SKIPPING_ALL_LOOKUPS_IN_IT,
                    &[&node_modules_at_types],
                );
                return continue_searching();
            }
            let name = self.name.clone();
            let mangled = self.mangle_scoped_package_name(&name);
            return self.load_module_from_specific_node_modules_directory(
                Extensions::DECLARATION,
                &mangled,
                &node_modules_at_types,
            );
        }

        continue_searching()
    }

    // Go: internal/module/resolver.go:resolutionState.loadModuleFromSpecificNodeModulesDirectory
    pub(crate) fn load_module_from_specific_node_modules_directory(
        &mut self,
        ext: Extensions,
        module_name: &str,
        node_modules_directory: &str,
    ) -> Resolved {
        // Strip any trailing separator so `pkg/` and `pkg` produce identical
        // `candidate`/`packageDirectory` strings (TS issue #3526).
        let candidate = tspath::remove_trailing_directory_separator(&tspath::normalize_path(
            &tspath::combine_paths(node_modules_directory, &[module_name]),
        ))
        .to_string();
        let (package_name, rest) = parse_package_name(module_name);
        let mut package_directory = tspath::combine_paths(node_modules_directory, &[&package_name]);
        if package_name.is_empty() {
            package_directory = candidate.clone();
        }

        if self.resolve_package_directory_only {
            if self.resolver.host.fs().directory_exists(&package_directory) {
                return Some(ResolvedInner {
                    path: package_directory,
                    ..Default::default()
                });
            }
            return continue_searching();
        }

        let mut root_package_info: Option<PackageJsonInfo> = None;
        // First look for a nested package.json (e.g. node_modules/foo/bar/package.json).
        let mut package_info = self.get_package_json_info(&candidate);
        if !rest.is_empty() && package_info.as_ref().is_some_and(|p| p.exists()) {
            if self.features.contains(NodeResolutionFeatures::EXPORTS) {
                root_package_info = self.get_package_json_info(&package_directory);
            }
            let root_has_exports = root_package_info.as_ref().is_some_and(|p| {
                p.exists()
                    && p.contents().fields().path.exports.value_type() != JsonValueType::NotPresent
            });
            if !root_has_exports {
                let from_file = self.load_module_from_file(ext, &candidate);
                if !from_file.should_continue_searching() {
                    return from_file;
                }
                let mut from_directory = self.load_node_module_from_directory_worker(
                    ext,
                    &candidate,
                    package_info.as_ref(),
                );
                if !from_directory.should_continue_searching() {
                    let path = from_directory.as_ref().expect("some").path.clone();
                    let pkg_id = self.get_package_id(&path, package_info.as_ref());
                    from_directory.as_mut().expect("some").package_id = pkg_id;
                    return from_directory;
                }
            }
        }

        if !rest.is_empty() {
            package_info = root_package_info.take();
            if package_info.is_none() {
                package_info = self.get_package_json_info(&package_directory);
            }
        }

        if let Some(info) = package_info.as_ref() {
            self.resolved_package_directory = true;
            let exports_usable = self.features.contains(NodeResolutionFeatures::EXPORTS)
                && info.exists()
                && !info.contents().fields().path.exports.is_falsy();
            if exports_usable {
                let subpath = tspath::combine_paths(".", &[&rest]);
                return self.load_module_from_exports(info, ext, &subpath);
            }
            if !rest.is_empty() {
                let version_paths = self.version_paths_of(info.contents());
                if version_paths.exists() {
                    if self.tracer.is_some() {
                        let version = version_paths.version().to_string();
                        write_trace(
                            &mut self.tracer,
                            &tsgo_diagnostics::X_PACKAGE_JSON_HAS_A_TYPESVERSIONS_ENTRY_0_THAT_MATCHES_COMPILER_VERSION_1_LOOKING_FOR_A_PATTERN_TO_MATCH_MODULE_NAME_2,
                            &[&version, tsgo_core::version::version(), &rest],
                        );
                    }
                    let path_patterns = try_parse_patterns(version_paths.get_paths());
                    let loader = Loader::SpecificNodeModules {
                        package_info: package_info.as_ref(),
                        rest: &rest,
                    };
                    let from_paths = self.try_load_module_using_paths(
                        ext,
                        &rest,
                        &package_directory,
                        version_paths.get_paths(),
                        &path_patterns,
                        &loader,
                    );
                    if !from_paths.should_continue_searching() {
                        return from_paths;
                    }
                }
            }
        }

        self.specific_node_modules_loader(ext, &candidate, package_info.as_ref(), &rest)
    }

    // Go: internal/module/resolver.go:loadModuleFromSpecificNodeModulesDirectory's inner loader
    pub(crate) fn specific_node_modules_loader(
        &mut self,
        extensions: Extensions,
        candidate: &str,
        package_info: Option<&PackageJsonInfo>,
        rest: &str,
    ) -> Resolved {
        if !rest.is_empty() || !self.esm_mode {
            let mut from_file = self.load_module_from_file(extensions, candidate);
            if !from_file.should_continue_searching() {
                let path = from_file.as_ref().expect("some").path.clone();
                let pkg_id = self.get_package_id(&path, package_info);
                from_file.as_mut().expect("some").package_id = pkg_id;
                return from_file;
            }
        }
        let mut from_directory =
            self.load_node_module_from_directory_worker(extensions, candidate, package_info);
        if !from_directory.should_continue_searching() {
            let path = from_directory.as_ref().expect("some").path.clone();
            let pkg_id = self.get_package_id(&path, package_info);
            from_directory.as_mut().expect("some").package_id = pkg_id;
            return from_directory;
        }
        // EsmMode disables index lookups generally, but a non-relative package
        // still assumes a default `index.js` when neither `main` nor `exports`
        // are present.
        let exports_empty_or_null = package_info.is_some_and(|p| {
            p.exists()
                && matches!(
                    p.contents().fields().path.exports.value_type(),
                    JsonValueType::NotPresent | JsonValueType::Null
                )
        });
        if rest.is_empty() && exports_empty_or_null && self.esm_mode {
            let index = tspath::combine_paths(candidate, &["index.js"]);
            let mut index_result = self.load_module_from_file(extensions, &index);
            if !index_result.should_continue_searching() {
                let path = index_result.as_ref().expect("some").path.clone();
                let pkg_id = self.get_package_id(&path, package_info);
                index_result.as_mut().expect("some").package_id = pkg_id;
                return index_result;
            }
        }
        continue_searching()
    }
}

