use super::*;
use crate::try_parse_version;

fn assert_ranges_good_bad(range_str: &str, good: &[&str], bad: &[&str]) {
    let (version_range, ok) = try_parse_version_range(range_str);
    assert!(ok, "{range_str}");
    for g in good {
        let v = try_parse_version(g).expect(g);
        assert!(
            version_range.test(&v),
            "{g} should be matched by range {range_str}"
        );
    }
    for b in bad {
        let v = try_parse_version(b).expect(b);
        assert!(
            !version_range.test(&v),
            "{b} should not be matched by range {range_str}"
        );
    }
}

fn assert_range_test(range_str: &str, version_str: &str, expected: bool) {
    let (version_range, ok) = try_parse_version_range(range_str);
    assert!(ok, "{range_str}");
    let v = try_parse_version(version_str).expect(version_str);
    assert_eq!(
        version_range.test(&v),
        expected,
        "version {version_str} in range {range_str}"
    );
}

fn assert_all_same_string(strs: &[&str]) {
    for s1 in strs {
        for s2 in strs {
            let (v1, ok1) = try_parse_version_range(s1);
            assert!(ok1, "{s1}");
            let (v2, ok2) = try_parse_version_range(s2);
            assert!(ok2, "{s2}");
            assert_eq!(v1.to_string(), v2.to_string(), "{s1} == {s2}");
        }
    }
}

