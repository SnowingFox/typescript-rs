//! The full formatting rule table.
//!
//! 1:1 port of Go `internal/format/rules.go`: [`get_all_rules`] builds every
//! formatting rule (high-priority common, user-configurable, then low-priority
//! common), plus the token-range constructors.
//!
//! # `ALL_TOKEN_KINDS`
//!
//! Go iterates `for token := KindFirstToken; token <= KindLastToken; token++`.
//! The Rust `tsgo_ast::Kind` enum has no `i16 -> Kind` reverse map, so the token
//! range `[Unknown ..= DeferKeyword]` is materialized as the
//! [`ALL_TOKEN_KINDS`] table. A self-check test asserts the table is contiguous
//! and ends at `KindLastToken`, guarding against drift from `tsgo_ast`.

