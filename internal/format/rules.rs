    let unary_prefix_expressions = vec![
        Kind::NumericLiteral,
        Kind::BigIntLiteral,
        Kind::Identifier,
        Kind::OpenParenToken,
        Kind::OpenBracketToken,
        Kind::OpenBraceToken,
        Kind::ThisKeyword,
        Kind::NewKeyword,
    ];
    let unary_preincrement_expressions = vec![
        Kind::Identifier,
        Kind::OpenParenToken,
        Kind::ThisKeyword,
        Kind::NewKeyword,
    ];
    let unary_postincrement_expressions = vec![
        Kind::Identifier,
        Kind::CloseParenToken,
        Kind::CloseBracketToken,
        Kind::NewKeyword,
    ];
    let unary_predecrement_expressions = vec![
        Kind::Identifier,
        Kind::OpenParenToken,
        Kind::ThisKeyword,
        Kind::NewKeyword,
    ];
    let unary_postdecrement_expressions = vec![
        Kind::Identifier,
        Kind::CloseParenToken,
        Kind::CloseBracketToken,
        Kind::NewKeyword,
    ];
    let comments = vec![Kind::SingleLineCommentTrivia, Kind::MultiLineCommentTrivia];
    let type_keywords = vec![
        Kind::AnyKeyword,
        Kind::AssertsKeyword,
        Kind::BigIntKeyword,
        Kind::BooleanKeyword,
        Kind::FalseKeyword,
        Kind::InferKeyword,
        Kind::KeyOfKeyword,
        Kind::NeverKeyword,
        Kind::NullKeyword,
        Kind::NumberKeyword,
        Kind::ObjectKeyword,
        Kind::ReadonlyKeyword,
        Kind::StringKeyword,
        Kind::SymbolKeyword,
        Kind::TypeOfKeyword,
        Kind::TrueKeyword,
        Kind::VoidKeyword,
        Kind::UndefinedKeyword,
        Kind::UniqueKeyword,
        Kind::UnknownKeyword,
    ];
    let mut type_names = vec![Kind::Identifier];
    type_names.extend_from_slice(&type_keywords);

    // Place a space before open brace in a function declaration.
    let function_open_brace_left_token_range = any_token_including_multiline_comments.clone();
    // Place a space before open brace in a TS decl with braces as children.
    let typescript_open_brace_left_token_range = token_range_from(&[
        Kind::Identifier,
        Kind::GreaterThanToken,
        Kind::MultiLineCommentTrivia,
        Kind::ClassKeyword,
        Kind::ExportKeyword,
        Kind::ImportKeyword,
    ]);
    // Place a space before open brace in a control-flow construct.
    let control_open_brace_left_token_range = token_range_from(&[
        Kind::CloseParenToken,
        Kind::MultiLineCommentTrivia,
        Kind::DoKeyword,
        Kind::TryKeyword,
        Kind::FinallyKeyword,
        Kind::ElseKeyword,
        Kind::CatchKeyword,
    ]);

